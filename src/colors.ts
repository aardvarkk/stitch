export type DMC = {
  dmc: string;
  name: string;
  hex: string;
  r: number;
  g: number;
  b: number;
};

// rgbHex: "#abc123"
export function getClosestDMC(rgbHex: string) {
  const target = colord(rgbHex);
  let minDiff = Number.MAX_VALUE;
  let match: DMC;
  for (const c of DMC_COLORS) {
    const candidate = colord(c.hex);
    const diff = target.delta(candidate);
    if (diff < minDiff) {
      match = c;
      minDiff = diff;
    }
  }
  return match!;
}

// Sort by
function sortDMC(a: DMC, b: DMC) {
  return DMC_ORDER.indexOf(a.dmc) - DMC_ORDER.indexOf(b.dmc);
}

